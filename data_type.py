# string
print("Hello world")
print("Hello world")
print("""Hello world""")
print("""Hello world""")

# para ver el tipo de dato
print(type("Hello world"))

# concatenar
print("Bye" + " " + "world")


# Numbers

# Integer
print(30)
print(type(30))

# Float
print(30.5)
print(type(30.5))


# Boolean
print(type(True))
print(type(False))


# List
[20, 30, 40, 50]
print(type([20, 30, 40, 50]))
["Hello", "Bye", "Adiós"]
print(type(["Hello", "Bye", "Adiós"]))


# Tuples

(10, 20, 30, 40)
print(type((10, 20, 30, 40)))


# Dictionaries

{"name": "Yuli", "surname": "Castro", "nickname": "Pame"}
print(type({"name": "Yuli", "surname": "Castro", "nickname": "Pame"}))


# None

print(type(None))
